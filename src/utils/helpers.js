// Generar iniciales para avatar
export function getInitials(nombre, apellido) {
    const n = (nombre || '').charAt(0).toUpperCase();
    const a = (apellido || '').charAt(0).toUpperCase();
    return n + a;
}

// Generar color basado en string
export function stringToColor(str) {
    const colors = [
        'linear-gradient(135deg, #7c3aed, #a78bfa)',
        'linear-gradient(135deg, #2563eb, #60a5fa)',
        'linear-gradient(135deg, #059669, #34d399)',
        'linear-gradient(135deg, #d97706, #fbbf24)',
        'linear-gradient(135deg, #dc2626, #f87171)',
        'linear-gradient(135deg, #db2777, #f472b6)',
        'linear-gradient(135deg, #0891b2, #22d3ee)',
        'linear-gradient(135deg, #4f46e5, #818cf8)',
    ];
    let hash = 0;
    for (let i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash);
    }
    return colors[Math.abs(hash) % colors.length];
}

// Validar DNI (8 dígitos numéricos)
export function validarDNI(dni) {
    return /^\d{7,8}$/.test(dni);
}

// Formatear fecha
export function formatDate(dateStr) {
    if (!dateStr) return '-';
    const date = new Date(dateStr);
    return date.toLocaleDateString('es-AR', { day: '2-digit', month: '2-digit', year: 'numeric' });
}

// Sanitizar input
export function sanitize(str) {
    const div = document.createElement('div');
    div.textContent = str;
    return div.innerHTML;
}

// Generar UUID simple
export function generateId() {
    return 'id-' + Date.now().toString(36) + '-' + Math.random().toString(36).substr(2, 9);
}

// Mostrar toast
export function showToast(message, type = 'success') {
    let container = document.querySelector('.toast-container');
    if (!container) {
        container = document.createElement('div');
        container.className = 'toast-container';
        document.body.appendChild(container);
    }

    const toast = document.createElement('div');
    toast.className = `toast toast-${type}`;
    toast.innerHTML = `
    <span>${type === 'success' ? icons.check : icons.warning}</span>
    <span>${sanitize(message)}</span>
  `;
    container.appendChild(toast);

    setTimeout(() => {
        toast.style.opacity = '0';
        toast.style.transform = 'translateX(50px)';
        toast.style.transition = 'all 0.3s ease';
        setTimeout(() => toast.remove(), 300);
    }, 3000);
}

// Crear modal
export function createModal(title, contentHTML, footerHTML = '', maxWidth = '480px') {
    const overlay = document.createElement('div');
    overlay.className = 'modal-overlay';
    overlay.innerHTML = `
    <div class="modal" style="max-width:${maxWidth};">
      <div class="modal-header">
        <h3 class="modal-title">${title}</h3>
        <button class="modal-close" id="modal-close-btn">${icons.close}</button>
      </div>
      <div class="modal-body">${contentHTML}</div>
      ${footerHTML ? `<div class="modal-footer">${footerHTML}</div>` : ''}
    </div>
  `;


    document.body.appendChild(overlay);

    overlay.querySelector('#modal-close-btn').addEventListener('click', () => overlay.remove());
    overlay.addEventListener('click', (e) => {
        if (e.target === overlay) overlay.remove();
    });

    return overlay;
}

// Crear diálogo de confirmación
export function confirmDialog(message, onConfirm) {
    const overlay = createModal('Confirmar', `
    <div class="confirm-dialog">
      <div class="confirm-icon">${icons.warning}</div>
      <p class="confirm-text">${message}</p>
      <div class="confirm-actions">
        <button class="btn btn-secondary" id="confirm-cancel">Cancelar</button>
        <button class="btn btn-danger" id="confirm-ok">Eliminar</button>
      </div>
    </div>
  `);

    overlay.querySelector('#confirm-cancel').addEventListener('click', () => overlay.remove());
    overlay.querySelector('#confirm-ok').addEventListener('click', () => {
        onConfirm();
        overlay.remove();
    });
}
